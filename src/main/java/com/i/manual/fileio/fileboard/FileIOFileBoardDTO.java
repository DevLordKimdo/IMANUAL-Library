package com.i.manual.fileio.fileboard;

public class FileIOFileBoardDTO {
	
	private String idx;
	private String postidx;
	private String name;
	private String namecrypto;
	private String size;
	private String seq;
	private String datetime;
	private String postcount;
	
	public String getIdx() {
		return idx;
	}
	public void setIdx(String idx) {
		this.idx = idx;
	}
	public String getPostidx() {
		return postidx;
	}
	public void setPostidx(String postidx) {
		this.postidx = postidx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNamecrypto() {
		return namecrypto;
	}
	public void setNamecrypto(String namecrypto) {
		this.namecrypto = namecrypto;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getSeq() {
		return seq;
	}
	public void setSeq(String seq) {
		this.seq = seq;
	}
	public String getDatetime() {
		return datetime;
	}
	public void setDatetime(String datetime) {
		this.datetime = datetime;
	}
	public String getPostcount() {
		return postcount;
	}
	public void setPostcount(String postcount) {
		this.postcount = postcount;
	}

}
